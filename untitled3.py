        "Günlük temizlik malzemesi kullanımınız nedir? (litre)",
        "Günlük ofis malzemesi kullanımınız nedir? (adet)",
        "Günlük diş fırçalama sayınız nedir? (adet)",
        "Günlük bilgisayar kullanım süreniz nedir? (saat)",
