        "Günlük benzin tüketiminiz nedir? (litre)",
        "Metreküp doğalgaz başına aylık tüketiminiz nedir? (m^3)",
        "Haftada fırında yemek pişirme süreniz nedir?(saat)",
        "Günde kaç saat uyursunuz(saat)",
        "Günde kaç fincan kahve içersiniz? ",
        "Günlük bisiklet kullanım süreniz nedir? (saat)",
        "Günlük duş alma süreniz nedir? (dakika)",
        "Günlük şarj ettiğiniz elektronik cihaz sayısı nedir? (adet)",
        "Günlük kitap okuma süreniz nedir? (saat)",
